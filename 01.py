        with st.spinner("正在深度拆解你的特质，匹配最契合的人物..."):
            try:
                response = client.chat.completions.create(
                    model="deepseek-chat",
                    messages=[{"role": "user", "content": prompt}],
                    temperature=0.85,
                    max_tokens=700,
                    stream=False
                )
                st.divider()
                st.subheader("✨ 你的专属匹配结果")
                st.write(response.choices[0].message.content)
            except Exception as e:
                st.error(f"生成失败，请稍后再试～错误信息：{e}")